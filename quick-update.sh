# Quick Update Script for GetCash Repository

# After making changes in VS Code, run these commands to update GitHub:

# 1. Add all changed files
git add .

# 2. Commit with a message
git commit -m "Update: Made changes to improve user experience"

# 3. Push to GitHub
git push origin main

# You can also use this one-liner:
# git add . && git commit -m "Quick update" && git push origin main

echo "✅ Your changes have been pushed to GitHub!"
echo "🌐 Visit: https://github.com/buttuura/GetCash"
echo "🚀 Deploy updates on Render.com will happen automatically!"